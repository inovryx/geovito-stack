'use strict';

const SUPPORTED_LANGUAGES = ['en', 'de', 'es', 'ru', 'zh-cn'];

const languageKey = (language) => (language === 'zh-cn' ? 'zhCn' : language);

const missingTranslation = (language) => ({
  language,
  status: 'missing',
  runtime_translation: false,
  indexable: false,
  last_reviewed_at: null,
});

const completeTranslation = ({
  language,
  title,
  slug,
  excerpt,
  body,
  canonicalPath,
  lastReviewedAt = '2026-02-08T00:00:00.000Z',
}) => ({
  language,
  status: 'complete',
  title,
  slug,
  excerpt,
  body,
  canonical_path: canonicalPath,
  runtime_translation: false,
  indexable: true,
  last_reviewed_at: lastReviewedAt,
  seo: {
    metaTitle: title,
    metaDescription: excerpt,
  },
});

const draftTranslation = ({ language, title, slug, excerpt, body }) => ({
  language,
  status: 'draft',
  title,
  slug,
  excerpt,
  body,
  runtime_translation: false,
  indexable: false,
  last_reviewed_at: null,
});

const buildTranslations = (input) =>
  SUPPORTED_LANGUAGES.map((language) => {
    const key = languageKey(language);
    const value = input[key];
    if (!value) return missingTranslation(language);
    if (value.kind === 'draft') {
      return draftTranslation({ language, ...value });
    }
    return completeTranslation({ language, ...value });
  });

